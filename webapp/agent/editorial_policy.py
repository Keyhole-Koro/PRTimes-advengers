def _format_section(name: str, items: Iterable[str]) -> str:
    bullet_lines = "\n".join(f"- {item}" for item in items)
    return f"{name}:\n{bullet_lines}"


def render_editorial_policy() -> str:
    sections = []
    for name, items in EDITORIAL_POLICY.items():
        sections.append(_format_section(name, items))
    return "\n\n".join(sections)
